class Solution{
  public:
    TNode* sortedListToBST(LNode *head) {
        if(!head) return NULL;
        vector<int> nums;
        while(head)
        {
            nums.push_back(head->data);
            head = head->next;
        }
        
        int n = nums.size();
        TNode* res =  helper(0,n-1, nums);
        return res;
    }
    TNode* helper(int l , int r, vector<int>&nums)
    {
        if(l>r) return NULL;
        int m =ceil((float) (l+r)/2);
        TNode *root = new TNode(nums[m]);
        root->left = helper(l,m-1,nums);
         root->right = helper(m+1,r,nums);
        return root;
    }
};

